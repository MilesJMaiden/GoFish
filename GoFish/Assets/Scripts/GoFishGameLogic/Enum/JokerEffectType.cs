/// <summary>
/// Defines the types of effects a Joker card can have based on its suit.
/// </summary>
public enum JokerEffectType
{
    HeartsEffect,   // Effect associated with the Joker of Hearts.
    DiamondsEffect, // Effect associated with the Joker of Diamonds.
    ClubsEffect,    // Effect associated with the Joker of Clubs.
    SpadesEffect    // Effect associated with the Joker of Spades.
}